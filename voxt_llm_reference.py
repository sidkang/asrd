"""
Reference implementation of VoxT's ASR -> LLM chains.

This is intentionally independent from VoxT. It mirrors the main prompt-shaping
logic for three core workflows so we can compare local/remote LLM behavior:

- transcription enhancement: ASR text -> cleaned text
- translation: source text -> translated text
- rewrite: spoken instruction + selected text -> rewritten text

It exposes API endpoints only for now. Defaults are based on VoxT's public
source, especially AppPreferenceKey.swift and LLMExecutionPlanCompiler.swift.
"""

from __future__ import annotations

import argparse
import json
import os
from enum import Enum
from typing import Any, Literal

import httpx
from fastapi import FastAPI, File, Form, HTTPException, UploadFile
from fastapi.responses import JSONResponse
from jinja2 import Environment, StrictUndefined
from pydantic import BaseModel, Field

DEFAULT_HOST = "127.0.0.1"
DEFAULT_PORT = 5112
DEFAULT_UPSTREAM_URL = os.environ.get("VOXT_LLM_UPSTREAM_URL", "")
DEFAULT_UPSTREAM_API_KEY = os.environ.get("VOXT_LLM_UPSTREAM_API_KEY", "")
DEFAULT_MODEL = os.environ.get("VOXT_LLM_MODEL", "")
DEFAULT_WHISPER_URL = os.environ.get("VOXT_WHISPER_URL", "http://127.0.0.1:5100/v1/audio/transcriptions")
DEFAULT_WHISPER_API_KEY = os.environ.get("VOXT_WHISPER_API_KEY", "password")
DEFAULT_WHISPER_MODEL = os.environ.get("VOXT_WHISPER_MODEL", "mlx-community/Qwen3-ASR-1.7B-6bit")

