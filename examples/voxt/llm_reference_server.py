def render_template(template: str, variables: dict[str, Any]) -> str:
    env = Environment(undefined=StrictUndefined, autoescape=False)
    return env.from_string(template).render(**variables).strip()


def normalized_other_languages(values: list[str]) -> str:
    cleaned = [value.strip() for value in values if value and value.strip()]
    return ", ".join(cleaned) if cleaned else "None specified"


def prompt_variables(
    *,
    user_main_language: str,
    user_other_languages: list[str] | None = None,
    target_language: str | None = None,
    dictionary_terms: str = "",
) -> dict[str, Any]:
    return {
        "USER_MAIN_LANGUAGE": user_main_language.strip() or "Chinese",
        "USER_OTHER_LANGUAGES": normalized_other_languages(user_other_languages or []),
        "TARGET_LANGUAGE": (target_language or "").strip(),
        "DICTIONARY_TERMS": dictionary_terms.strip(),
    }


def request_prompt_variables(req: BasePipelineRequest, *, target_language: str | None = None) -> dict[str, Any]:
    return prompt_variables(
        user_main_language=req.user_main_language,
        user_other_languages=req.user_other_languages,
        target_language=target_language,
        dictionary_terms=req.dictionary_terms,
    )


def join_sections(sections: list[str]) -> str:
    return "\n\n".join(s.strip() for s in sections if s and s.strip())


def glossary_block(text: str) -> str:
    text = text.strip()
    if not text:
        return ""
    return f"### Dictionary glossary\n{text}"


def conversation_block(turns: list[RewriteTurn]) -> str:
    if not turns:
        return ""
    parts = []
    for i, turn in enumerate(turns, 1):
        parts.append(
            f"Turn {i}:\n"
            f"Spoken instruction: {turn.user_prompt_text}\n"
            f"Previous title: {turn.result_title}\n"
            f"Previous content:\n{turn.result_content}"
        )
    return "### Rewrite conversation history\n" + "\n\n".join(parts)


def request_prompt(task: str, **kwargs: Any) -> str:
    if task == "enhancement":
        return f"""Process this ASR transcription according to the system instructions.
Return only the final processed text.
Input:
{kwargs['text']}"""
    if task == "translation":
        return f"""Translate this text.
Input:
{kwargs['text']}"""
    if task == "rewrite":
        dictated = kwargs["dictated_prompt"]
        source = kwargs.get("source_text", "").strip()
        if not source:
            return dictated
        return f"""Return the final text to insert.
Spoken instruction:
{dictated}

Selected source text:
{source}"""
    raise ValueError(f"unknown task: {task}")


def compile_request(task: str, req: BasePipelineRequest, prompt_content: str, **task_data: Any) -> CompiledRequest:
    compiled = compile_single_request(task, req, prompt_content, task_data)
    for index, segment_data in enumerate(segment_task_data(task, req, task_data), start=1):
        segment_compiled = compile_single_request(task, req, prompt_content, segment_data)
        compiled.segments.append(
            {
                "index": index,
                "input_chars": primary_input_length(task, segment_data),
                "task_data": segment_data,
                "compiled": segment_compiled.model_dump(),
            }
        )
    return compiled


def compile_single_request(task: str, req: BasePipelineRequest, prompt_content: str, task_data: dict[str, Any]) -> CompiledRequest:
    # Mirrors VoxT's LLMExecutionPlanCompiler behavior closely enough for
    # reference/comparison use.
    context_sections = [glossary_block(req.dictionary_glossary)]
    if isinstance(req, RewriteRequest):
        context_sections.append(conversation_block(req.conversation_history))
    context = join_sections(context_sections)

    if req.delivery == Delivery.user_message:
        instructions = context
        prompt = prompt_content
    else:
        instructions = join_sections([prompt_content, context])
        prompt = request_prompt(task, **task_data)

    messages = []
    if instructions.strip():
        messages.append({"role": "system", "content": instructions})
    messages.append({"role": "user", "content": prompt})

    payload = build_openai_payload(req, task, messages, instructions, prompt, task_data)
    return CompiledRequest(task=task, instructions=instructions, prompt=prompt, messages=messages, openai_payload=payload)


def primary_input_length(task: str, task_data: dict[str, Any]) -> int:
    if task in {"enhancement", "translation"}:
        return len(str(task_data.get("text") or ""))
    # Rewrite segmentation only splits selected source text. A long spoken
    # instruction is kept intact because splitting instructions changes intent.
    return len(str(task_data.get("source_text") or ""))


def segment_task_data(task: str, req: BasePipelineRequest, task_data: dict[str, Any]) -> list[dict[str, Any]]:
    if not req.enable_segmentation or primary_input_length(task, task_data) <= req.long_text_threshold:
        return []
    limit = max(80, req.segment_character_limit)
    if task in {"enhancement", "translation"}:
        text = str(task_data.get("text") or "")
        return [{**task_data, "text": segment} for segment in split_text_segments(text, limit)]
    source = str(task_data.get("source_text") or "")
    if not source.strip():
        return []
    return [{**task_data, "source_text": segment} for segment in split_text_segments(source, limit)]


def split_text_segments(text: str, limit: int) -> list[str]:
    text = text.strip()
    if len(text) <= limit:
        return [text] if text else []

    # Keep English period boundaries intact to avoid fragmenting abbreviations
    # such as "Dr. Smith" or "U.S. policy". Hard wrapping below still handles
    # long English paragraphs.
    parts = re.split(r"(\n\n+|(?<=[。！？!?])\s+)", text)
    candidates: list[str] = []
    current = ""
    for i in range(0, len(parts), 2):
        piece = parts[i]
        sep_before = parts[i - 1] if i > 0 else ""
        candidate = (current + sep_before + piece).strip() if current else piece.strip()
        if candidate and len(candidate) <= limit:
            current = candidate
            continue
        if current:
            candidates.append(current)
            current = ""
        if len(piece) <= limit:
            current = piece.strip()
        else:
            candidates.extend(hard_wrap(piece, limit))
    if current:
        candidates.append(current)
    return [segment for segment in candidates if segment.strip()]


def hard_wrap(text: str, limit: int) -> list[str]:
    chunks = []
    remaining = text.strip()
    while remaining:
        if len(remaining) <= limit:
            chunks.append(remaining)
            break
        cut = remaining.rfind(" ", 0, limit)
        if cut < limit // 2:
            cut = limit
        chunks.append(remaining[:cut].strip())
        remaining = remaining[cut:].strip()
    return chunks


def build_openai_payload(
    req: BasePipelineRequest,
    task: str,
    messages: list[dict[str, str]],
    instructions: str,
    prompt: str,
    task_data: dict[str, Any],
) -> dict[str, Any]:
    model = req.model or DEFAULT_MODEL
    if not model:
        model = "local-model"
    tuning = voxt_runtime_tuning(req, task, instructions, prompt, task_data)
    payload: dict[str, Any] = {"model": model, "messages": messages}
    payload["max_tokens"] = tuning.max_tokens
    payload["temperature"] = tuning.temperature
    payload["top_p"] = tuning.top_p
    if isinstance(req, RewriteRequest) and req.structured_answer_output and req.provider == VoxtLLMProvider.deepseek:
        payload["response_format"] = {"type": "json_object"}
    return payload


def voxt_runtime_tuning(
    req: BasePipelineRequest,
    task: str,
    instructions: str,
    prompt: str,
    task_data: dict[str, Any],
) -> VoxtRuntimeTuning:
    max_tokens = req.tuning.max_tokens or estimated_voxt_output_budget(task, instructions, prompt, task_data)
    if req.provider == VoxtLLMProvider.volcengine:
        default_temperature, default_top_p = 0.1, 0.3
    elif req.provider == VoxtLLMProvider.zai:
        default_temperature, default_top_p = 0.2, 0.7
    else:
        default_temperature, default_top_p = 0.2, 0.9
    return VoxtRuntimeTuning(
        max_tokens=max_tokens,
        temperature=req.tuning.temperature if req.tuning.temperature is not None else default_temperature,
        top_p=req.tuning.top_p if req.tuning.top_p is not None else default_top_p,
    )


def estimated_voxt_output_budget(task: str, instructions: str, prompt: str, task_data: dict[str, Any]) -> int:
    # VoxT estimates an output budget from input/prompt size. This is a compact
    # approximation for reference testing, not an exact token counter.
    if task == "enhancement":
        primary_chars = len(str(task_data.get("text") or ""))
        multiplier = 1.10
    elif task == "translation":
        primary_chars = len(str(task_data.get("text") or ""))
        multiplier = 1.35
    else:
        primary_chars = len(str(task_data.get("dictated_prompt") or "")) + len(str(task_data.get("source_text") or ""))
        multiplier = 1.35
    content_budget = int(max(1, primary_chars) * multiplier + 0.999)
    prompt_overhead = max(64, int((len(instructions) + len(prompt)) / 12))
    return max(256, min(32768, content_budget + prompt_overhead))


def extract_text(data: Any) -> str:
    if isinstance(data, dict):
        choices = data.get("choices")
        if isinstance(choices, list) and choices:
            msg = choices[0].get("message") if isinstance(choices[0], dict) else None
            if isinstance(msg, dict):
                content = msg.get("content")
                if isinstance(content, str):
                    return content.strip()
            text = choices[0].get("text") if isinstance(choices[0], dict) else None
            if isinstance(text, str):
                return text.strip()
        for key in ("output_text", "text", "content", "result"):
            value = data.get(key)
            if isinstance(value, str):
                return value.strip()
    return ""


def strip_code_fence(text: str) -> str:
    trimmed = text.strip()
    if not trimmed.startswith("```"):
        return trimmed
    lines = trimmed.splitlines()
    if len(lines) >= 2 and lines[-1].strip() == "```":
        return "\n".join(lines[1:-1]).strip()
    return trimmed


def parse_rewrite_payload(text: str) -> dict[str, str] | None:
    candidate = strip_code_fence(text)
    try:
        obj = json.loads(candidate)
    except Exception:
        obj = None
    if isinstance(obj, dict):
        title = str(obj.get("title") or "").strip()
        content = str(obj.get("content") or obj.get("text") or "").strip()
        if title or content:
            return {"title": title, "content": content}

    match = re.search(r"title\s*[:：]\s*(.*?)\n+content\s*[:：]\s*(.*)", candidate, flags=re.IGNORECASE | re.DOTALL)
    if match:
        return {"title": match.group(1).strip(), "content": match.group(2).strip()}
    return None


def sanitize_visible_output(task: str, text: str, *, fallback: str = "") -> tuple[str, dict[str, Any]]:
    metadata: dict[str, Any] = {"sanitized": False, "normalized": False}
    original = text.strip()
    cleaned = strip_code_fence(text)
    if cleaned != original:
        metadata["normalized"] = True

    if task == "rewrite":
        payload = parse_rewrite_payload(cleaned)
        if payload is not None:
            metadata["rewrite_answer_payload"] = payload
            content = payload.get("content", "").strip()
            metadata["sanitized"] = True
            return content, metadata

    before_prefix_cleanup = cleaned.strip()
    cleaned = re.sub(r"^(final\s+(answer|output)|final|output|result|content)\s*[:：]\s*", "", cleaned.strip(), flags=re.IGNORECASE)
    if cleaned != before_prefix_cleanup:
        metadata["sanitized"] = True
    if cleaned in {'""', "''", "{}", "[]"} or cleaned.lower() in {"null", "nil", "none", "n/a", "na"}:
        metadata["sanitized"] = True
        return fallback.strip(), metadata
    return cleaned, metadata


async def post_openai_payload(req: BasePipelineRequest, payload: dict[str, Any]) -> Any:
    url = req.upstream_url or DEFAULT_UPSTREAM_URL
    if not url:
        raise HTTPException(status_code=400, detail="upstream_url is required unless dry_run=true")
    headers = {"Content-Type": "application/json"}
    api_key = req.upstream_api_key or DEFAULT_UPSTREAM_API_KEY
    if api_key:
        headers["Authorization"] = f"Bearer {api_key}"
    async with httpx.AsyncClient(timeout=120) as client:
        response = await client.post(url, json=payload, headers=headers)
    if response.status_code >= 400:
        raise HTTPException(status_code=response.status_code, detail=response.text[:2000])
    return response.json()


async def execute(req: BasePipelineRequest, compiled: CompiledRequest) -> PipelineResponse:
    if req.dry_run:
        return PipelineResponse(task=compiled.task, text="", compiled=compiled, raw_response=None)
    if compiled.segments:
        outputs: list[str] = []
        raw_responses: list[Any] = []
        segment_metadata: list[dict[str, Any]] = []
        for segment in compiled.segments:
            segment_compiled = segment["compiled"]
            raw = await post_openai_payload(req, segment_compiled["openai_payload"])
            raw_responses.append(raw)
            fallback = segment.get("task_data", {}).get("text") or segment.get("task_data", {}).get("source_text") or ""
            sanitized, metadata = sanitize_visible_output(compiled.task, extract_text(raw), fallback=str(fallback))
            outputs.append(sanitized)
            segment_metadata.append({"index": segment["index"], **metadata})
        return PipelineResponse(
            task=compiled.task,
            text="\n\n".join(output for output in outputs if output.strip()),
            compiled=compiled,
            raw_response=raw_responses,
            metadata={"segmented": True, "segments": segment_metadata},
        )

    data = await post_openai_payload(req, compiled.openai_payload)
    sanitized, metadata = sanitize_visible_output(compiled.task, extract_text(data), fallback="")
    return PipelineResponse(task=compiled.task, text=sanitized, compiled=compiled, raw_response=data, metadata=metadata)


async def call_whisper_transcription(
    *,
    file: UploadFile,
    endpoint: str,
    api_key: str,
    model: str,
    filename: str,
    language: str | None,
    prompt: str | None,
    response_format: str,
    preview: bool,
) -> WhisperResponse:
    audio = await file.read()
    headers = {"Authorization": f"Bearer {api_key}"} if api_key else {}
    data = {"model": model, "response_format": response_format}
    if language:
        data["language"] = language
    if prompt:
        data["prompt"] = prompt
    files = {
        "file": (
            filename,
            audio,
            file.content_type or "application/octet-stream",
        )
    }
    async with httpx.AsyncClient(timeout=300) as client:
        response = await client.post(endpoint, data=data, files=files, headers=headers)
    if response.status_code >= 400:
        raise HTTPException(status_code=response.status_code, detail=response.text[:2000])
    content_type = response.headers.get("content-type", "")
    if "application/json" in content_type:
        raw: Any = response.json()
        text = extract_text(raw)
    else:
        raw = response.text
        text = response.text.strip()
    return WhisperResponse(
        text=text,
        endpoint=endpoint,
        model=model,
        filename=filename,
        preview=preview,
        raw_response=raw,
    )


