"""
Reference implementation of VoxT's ASR -> LLM chains.

This is intentionally independent from VoxT. It mirrors the main prompt-shaping
logic for three core workflows so we can compare local/remote LLM behavior:

- transcription enhancement: ASR text -> cleaned text
- translation: source text -> translated text
- rewrite: spoken instruction + selected text -> rewritten text

It exposes API endpoints only for now. Defaults are based on VoxT's public
source, especially AppPreferenceKey.swift and LLMExecutionPlanCompiler.swift.
"""

