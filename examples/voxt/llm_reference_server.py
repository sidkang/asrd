def language_code(value: str) -> str | None:
    return LANGUAGE_CODES.get(value.strip().lower()) if value and value.strip() else None


def compile_asr_hint(req: ASRHintRequest) -> ASRHintResponse:
    variables = prompt_variables(
        user_main_language=req.user_main_language,
        user_other_languages=req.user_other_languages,
        dictionary_terms=req.dictionary_terms,
    )
    template = req.prompt_template if req.prompt_template is not None else ASR_HINT_PROMPT
    rendered_prompt = render_template(template, variables)
    main_code = language_code(req.user_main_language)
    other_codes = [code for value in req.user_other_languages if (code := language_code(value))]
    all_codes = []
    for code in ([main_code] if main_code else []) + other_codes:
        if code not in all_codes:
            all_codes.append(code)
    explicit_single_language = req.follows_user_main_language and not req.user_other_languages

    response = ASRHintResponse(target=req.target, variables=variables)
    if req.target == ASRHintTarget.openai_whisper:
        response.language = main_code if explicit_single_language else None
        response.prompt = rendered_prompt or None
        response.multipart_fields = {"response_format": "json"}
        if response.language:
            response.multipart_fields["language"] = response.language
        if response.prompt:
            response.multipart_fields["prompt"] = response.prompt
        return response

    if req.target == ASRHintTarget.whisper_kit:
        # Voxt intentionally keeps local Whisper close to auto-detect mode.
        return response

    if req.target == ASRHintTarget.aliyun_fun:
        response.language_hints = all_codes[:3]
        response.language = response.language_hints[0] if response.language_hints else None
        response.parameters = {"sample_rate": 16000, "format": "pcm"}
        if response.language_hints:
            response.parameters["language_hints"] = response.language_hints
        if req.dictionary_terms.strip():
            response.parameters["hotwords"] = [line.strip() for line in req.dictionary_terms.splitlines() if line.strip()]
        return response

    # MLX/Qwen-style local ASR: provide language only for a single explicit
    # language, otherwise provide VoxT's multilingual context text.
    response.language = req.user_main_language if explicit_single_language else None
    if req.user_other_languages:
        response.multilingual_context = render_template(MULTILINGUAL_CONTEXT_PROMPT, variables)
    prompt_parts = [response.multilingual_context, rendered_prompt]
    response.prompt = "\n\n".join(part for part in prompt_parts if part) or None
    return response


def create_app() -> FastAPI:
    app = FastAPI(title="voxt-llm-reference")

    @app.get("/health")
    async def health():
        return {
            "status": "ok",
            "default_model": DEFAULT_MODEL,
            "default_upstream_url": DEFAULT_UPSTREAM_URL,
            "default_whisper_url": DEFAULT_WHISPER_URL,
            "default_whisper_model": DEFAULT_WHISPER_MODEL,
        }

    @app.post("/v1/voxt/whisper/transcribe", response_model=WhisperResponse)
    async def whisper_transcribe(
        file: UploadFile = File(...),
        endpoint: str = Form(DEFAULT_WHISPER_URL),
        api_key: str = Form(DEFAULT_WHISPER_API_KEY),
        model: str = Form(DEFAULT_WHISPER_MODEL),
        language: str | None = Form(None),
        prompt: str | None = Form(None),
        response_format: str = Form("json"),
    ):
        """Reference OpenAI/Whisper transcription call used by VoxT final uploads."""
        filename = file.filename or "voxt-reference-audio.wav"
        return await call_whisper_transcription(
            file=file,
            endpoint=endpoint,
            api_key=api_key,
            model=model,
            filename=filename,
            language=language,
            prompt=prompt,
            response_format=response_format,
            preview=False,
        )

    @app.post("/v1/voxt/whisper/chunk", response_model=WhisperResponse)
    async def whisper_chunk(
        file: UploadFile = File(...),
        endpoint: str = Form(DEFAULT_WHISPER_URL),
        api_key: str = Form(DEFAULT_WHISPER_API_KEY),
        model: str = Form(DEFAULT_WHISPER_MODEL),
        language: str | None = Form(None),
        prompt: str | None = Form(None),
        response_format: str = Form("json"),
    ):
        """Reference VoxT Chunk Pseudo Realtime Preview upload.

        VoxT's OpenAI preview path is still a Whisper transcription request, but
        it uses a `voxt-openai-preview-*.wav` filename. Our ASR server uses that
        filename to route the request to the preview model.
        """
        original = file.filename or "chunk.wav"
        suffix = ".wav"
        if "." in original:
            suffix = "." + original.rsplit(".", 1)[-1]
        filename = f"voxt-openai-preview-reference{suffix}"
        return await call_whisper_transcription(
            file=file,
            endpoint=endpoint,
            api_key=api_key,
            model=model,
            filename=filename,
            language=language,
            prompt=prompt,
            response_format=response_format,
            preview=True,
        )

    @app.post("/v1/voxt/asr-hint/compile", response_model=ASRHintResponse)
    async def asr_hint_compile(req: ASRHintRequest):
        """Compile VoxT-style ASR hint fields for Whisper, MLX/Qwen, or Aliyun Fun."""
        return compile_asr_hint(req)

    @app.post("/v1/voxt/transcription", response_model=PipelineResponse)
    async def transcription(req: EnhancementRequest):
        template = req.prompt_template or ENHANCEMENT_PROMPT
        prompt = render_template(template, request_prompt_variables(req))
        compiled = compile_request("enhancement", req, prompt, text=req.text)
        return await execute(req, compiled)

    @app.post("/v1/voxt/translation", response_model=PipelineResponse)
    async def translation(req: TranslationRequest):
        template = req.prompt_template or TRANSLATION_PROMPT
        prompt = render_template(
            template,
            request_prompt_variables(req, target_language=req.target_language),
        )
        compiled = compile_request("translation", req, prompt, text=req.text, target_language=req.target_language)
        return await execute(req, compiled)

    @app.post("/v1/voxt/rewrite", response_model=PipelineResponse)
    async def rewrite(req: RewriteRequest):
        template = req.prompt_template or REWRITE_PROMPT
        prompt = render_template(template, request_prompt_variables(req))
        compiled = compile_request(
            "rewrite",
            req,
            prompt,
            dictated_prompt=req.dictated_prompt,
            source_text=req.source_text,
        )
        return await execute(req, compiled)

    @app.post("/v1/voxt/compile/{task}")
    async def compile_only(task: Literal["transcription", "translation", "rewrite"], body: dict[str, Any]):
        if task == "transcription":
            req = EnhancementRequest(**body, dry_run=True)
            template = req.prompt_template or ENHANCEMENT_PROMPT
            prompt = render_template(template, request_prompt_variables(req))
            return compile_request("enhancement", req, prompt, text=req.text)
        if task == "translation":
            req = TranslationRequest(**body, dry_run=True)
            template = req.prompt_template or TRANSLATION_PROMPT
            prompt = render_template(template, request_prompt_variables(req, target_language=req.target_language))
            return compile_request("translation", req, prompt, text=req.text, target_language=req.target_language)
        req = RewriteRequest(**body, dry_run=True)
        template = req.prompt_template or REWRITE_PROMPT
        prompt = render_template(template, request_prompt_variables(req))
        return compile_request("rewrite", req, prompt, dictated_prompt=req.dictated_prompt, source_text=req.source_text)

    return app


def main() -> int:
    parser = argparse.ArgumentParser(description="Run VoxT-style ASR→LLM reference API.")
    parser.add_argument("--host", default=DEFAULT_HOST)
    parser.add_argument("--port", type=int, default=DEFAULT_PORT)
    parser.add_argument("--log-level", default="info")
    args = parser.parse_args()

    import uvicorn

    uvicorn.run(create_app(), host=args.host, port=args.port, log_level=args.log_level)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
